import UIKit

extension UIButton {
    
    func setSelectedColor() {
        
        self.backgroundColor = #colorLiteral(red: 1, green: 0.2527923882, blue: 1, alpha: 1)
    }
    
    func setDeselectedColor() {
        
        self.backgroundColor = #colorLiteral(red: 0.6642388105, green: 0.3087226748, blue: 0.657497108, alpha: 1)
    }
    
}
